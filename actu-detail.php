                <h1>Vers une nouvelle organisation des réunions des institutions représentatives du personnel ?</h1>
                <div class='content-default'>
                    <p>Si votre entreprise comporte au moins 50 salariés, vous disposez très certainement d’un comité d’entreprise et d’un comité d’hygiène et de sécurité. Chargé d’en assurer la présidence, vous devez connaître les nouvelles possibilités qui s’offrent à vous en matière de réunion de ces institutions…</p>

                    <h2>Des réunions en visioconférence, enregistrées et sténographiées</h2>
                    <p>Depuis août 2015, il est possible, avec l’accord des représentants du personnel concernés, de recourir à la visioconférence lors des réunions&nbsp;:</p>
                    <ul>
                        <li>du comité d’entreprise (CE),</li>
                        <li>du comité central d’entreprise,</li>
                        <li>du comité de groupe,</li>
                        <li>du comité d'entreprise européen,</li>
                        <li>du comité de la société européenne,</li>
                        <li>du comité d’hygiène, de sécurité et des conditions de travail (CHSCT),</li>
                        <li>de l’instance de coordination des CHSCT,</li>
                        <li>communes des institutions représentatives.</li>
                    </ul>
                    <p>Le système de visioconférence doit garantir <strong>l’identification des membres présents</strong> et le caractère secret de leur vote : c’est-à-dire que le dispositif ne doit pas mettre en relation l’identité de l’électeur et son vote. Les participants ont tous le même temps imparti pour voter.</p>
                    <p>A cela, ajoutons que les réunions du comité d’entreprise, comité central d’entreprise, comité de groupe, comité d’entreprise européen, comité de la société européenne, peuvent faire l’objet d’un enregistrement ou d’une sténographie. Si c’est le comité qui est à l’initiative de la demande, l’employeur ne peut s’y opposer que si les délibérations portent sur des <strong>informations confidentielles</strong>, qu’il a présentées comme telles.</p>
                    <p>Toute personne extérieure à l’entreprise, mandatée pour sténographier les séances du comité, est soumise à une obligation de discrétion. Elle est rémunérée par l’employeur si c’est lui qui est à l’initiative de sa production. Vraisemblablement, si c’est le CE qui en est à l’initiative, la personne extérieure sera rémunérée sur le budget de fonctionnement de l’institution.</p>
                    <p>Enfin, les délibérations du CE sont consignées dans un <strong>procès-verbal</strong> (PV), rédigé par le secrétaire du comité. Si aucun accord n’est trouvé entre l’employeur et les membres du CE, le PV, contenant le résumé des délibérations du CE ainsi que la décision motivée de l’employeur, doit être transmis à ce dernier dans un délai de :</p>
                    <ul>
                        <li>15 jours, en principe,</li>
                        <li>3 jours en cas de projet de licenciements économiques avec plan de sauvegarde de l’emploi,</li>
                        <li>1 jour lorsque la société est en redressement ou liquidation judiciaire.</li>
                    </ul>
                    <p>Notez que si une nouvelle réunion devait se tenir <strong>avant l’expiration de ces délais</strong>, le PV devrait être établi et transmis à l’employeur avant la date de cette nouvelle réunion.</p>
                    <p><b><u>Source :</u></b> Décret n° 2016-453 du 12 avril 2016 relatif à certaines modalités de déroulement des réunions des institutions représentatives du personnel</p>
